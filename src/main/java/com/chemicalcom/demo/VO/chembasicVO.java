package com.chemicalcom.demo.VO;

public class chembasicVO {

    String name;

    String otherName;

    String CAS;

    String MOLFOR;

    String MOLWEI;

    String dangerRank;

    String picture;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOtherName() {
        return otherName;
    }

    public void setOtherName(String otherName) {
        this.otherName = otherName;
    }

    public String getCAS() {
        return CAS;
    }

    public void setCAS(String CAS) {
        this.CAS = CAS;
    }

    public String getMOLFOR() {
        return MOLFOR;
    }

    public void setMOLFOR(String MOLFOR) {
        this.MOLFOR = MOLFOR;
    }

    public String getMOLWEI() {
        return MOLWEI;
    }

    public void setMOLWEI(String MOLWEI) {
        this.MOLWEI = MOLWEI;
    }

    public String getDangerRank() {
        return dangerRank;
    }

    public void setDangerRank(String dangerRank) {
        this.dangerRank = dangerRank;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }
}
